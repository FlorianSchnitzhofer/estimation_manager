def render_pdf(data: dict) -> bytes:
    from reportlab.lib import colors
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
    from reportlab.lib.units import mm
    from reportlab.platypus import (Paragraph, SimpleDocTemplate, Spacer, Table,
                                    TableStyle)

    t = data["t"]
    styles = getSampleStyleSheet()
    h1 = ParagraphStyle("H1", parent=styles["Heading1"], textColor=colors.black, spaceBefore=14)
    body = styles["BodyText"]
    small = ParagraphStyle("Small", parent=body, fontSize=8, leading=10)

    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4, leftMargin=18 * mm, rightMargin=18 * mm,
                            topMargin=18 * mm, bottomMargin=18 * mm,
                            title=f"{data['title']} - {dict(data['meta'])[t['project']]}")
    story = [Paragraph(data["title"], styles["Title"]),
             Paragraph(t["subtitle"], body), Spacer(1, 6)]

    grid = TableStyle([
        ("GRID", (0, 0), (-1, -1), 0.4, colors.HexColor("#9a9a9a")),
        ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#111111")),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
        ("FONTSIZE", (0, 0), (-1, -1), 8),
        ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ("LEFTPADDING", (0, 0), (-1, -1), 4),
        ("RIGHTPADDING", (0, 0), (-1, -1), 4),
    ])
    plain = TableStyle([
        ("GRID", (0, 0), (-1, -1), 0.4, colors.HexColor("#9a9a9a")),
        ("FONTSIZE", (0, 0), (-1, -1), 9),
        ("VALIGN", (0, 0), (-1, -1), "TOP"),
    ])

    def para_rows(rows):
        return [[Paragraph(str(c), small) for c in row] for row in rows]

    def add_table(header, rows, widths=None):
        story.append(Table([header] + para_rows(rows), style=grid, colWidths=widths,
                           repeatRows=1))
        story.append(Spacer(1, 6))

    story.append(Paragraph(t["s1"], h1))
    story.append(Table(data["meta"], style=plain, colWidths=[45 * mm, None]))

    story.append(Paragraph(t["s2"], h1))
    story.append(Paragraph(f"<b>{t['goal']}:</b> {data['goal']}", body))
    story.append(Paragraph(f"<b>{t['benefit']}:</b> {data['benefit']}", body))

    story.append(Paragraph(t["s3"], h1))
    add_table(data["param_header"], data["param_rows"], widths=[35 * mm, 15 * mm, None])

    story.append(Paragraph(t["s4"], h1))
    add_table(data["fp_header"], data["fp_rows"])
    for label, value in data["fp_summary"]:
        story.append(Paragraph(f"<b>{label}:</b> {value}", body))

    story.append(Paragraph(t["s5"], h1))
    add_table(data["variant_header"], data["variant_rows"])
    add_table(data["phase_header"], data["phase_rows"])

    story.append(Paragraph(t["s6"], h1))
    add_table(data["ops_header"], data["ops_rows"])
    story.append(Paragraph(data["token_line"], body))

    story.append(Paragraph(t["s7"], h1))
    for a in data["assumptions"]:
        story.append(Paragraph(f"• {a}", body))

    story.append(Paragraph(t["s8"], h1))
    story.append(Paragraph(data["out_of_scope"], body))

    story.append(Paragraph(t["s9"], h1))
    if data["history_rows"]:
        add_table(data["history_header"], data["history_rows"],
                  widths=[15 * mm, 30 * mm, 22 * mm, None, 18 * mm])
    else:
        story.append(Paragraph(data["no_sessions"], body))

    story.append(Paragraph(t["s10"], h1))
    for sig in data["signatures"]:
        story.append(Spacer(1, 24))
        story.append(Paragraph("_________________________________", body))
        story.append(Paragraph(sig, small))

    doc.build(story)
    return buf.getvalue()
