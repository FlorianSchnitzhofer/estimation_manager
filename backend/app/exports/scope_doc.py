def _fmt(n: float) -> str:
    return f"{n:,.0f}".replace(",", ".")  # 12.345 (EU grouping)


def _param_label(key: str, lang: str) -> str:
    p = next(p for p in PARAMETERS if p["key"] == key)
    return p["label"][lang]


def _phase_label(key: str, lang: str) -> str:
    p = next(p for p in PHASES if p["key"] == key)
    return p[lang]


def _variant_label(key: str, lang: str) -> str:
    v = next(v for v in VARIANTS if v["key"] == key)
    return v[lang]


def build_scope_data(project, sessions, results: dict, lang: str = "de") -> dict:
    """Language-resolved content model consumed by the DOCX and PDF renderers."""
    t = L[lang]
    params = normalize_params(
        (sessions[-1].params if sessions else project.params) or {}
    )
    fp = results["function_points"]
    version = f"{sessions[-1].version}.0" if sessions else "0.1"

    param_rows = [
        [_param_label(p["key"], lang), str(params[p["key"]]), p["definition"][lang]]
        for p in PARAMETERS
    ]

    fp_rows = [
        [
            _param_label(c["param"], lang), c["ifpug_type"], str(c["count"]),
            str(c["effective_count"]), t.get(f"complexity_{c['complexity']}", c["complexity"]),
            str(c["weight"]), _fmt(c["fp"]),
        ]
        for c in fp["components"]
    ]

    variant_rows, phase_rows, ops_rows = [], [], []
    for vkey in ("classic", "agile", "agentic"):
        v = results["variants"][vkey]
        variant_rows.append([
            _variant_label(vkey, lang), f"{v['hours_per_fp']} h/FP",
            _fmt(v["hours"]["best"]), _fmt(v["hours"]["expected"]), _fmt(v["hours"]["worst"]),
            _fmt(v["cost"]["expected"]),
        ])
        for ph in PHASES:
            p = v["phases"][ph["key"]]
            phase_rows.append([
                _variant_label(vkey, lang), _phase_label(ph["key"], lang),
                f"{p['pct']:.0f} %", _fmt(p["hours"]), _fmt(p["cost"]),
            ])
        ops_rows.append([
            _variant_label(vkey, lang),
            f"{v['operations']['pct_pa']:.0f} % {t['of_implementation']}",
            _fmt(v["operations"]["annual_cost"]),
            f"{v['consulting']['pct']:.0f} % {t['of_implementation']}",
            _fmt(v["consulting"]["cost"]),
        ])

    tokens = results["tokens"]
    token_line = (
        f"{t['tokens']}: {_fmt(tokens['total_tokens'])} Tokens "
        f"({_fmt(tokens['tokens_per_fp'])} Tokens/FP, "
        f"{tokens['price_per_million_eur']} EUR/1M) = {_fmt(tokens['cost'])} EUR"
    )

    history_rows = [
        [
            str(s.version), s.name, s.created_at.date().isoformat(),
            ", ".join(f"{_param_label(k, lang)}: {v}" for k, v in normalize_params(s.params).items() if v),
            _fmt(s.results["function_points"]["adjusted"]),
        ]
        for s in sessions
    ]

    assumptions = [a[lang] for a in results["assumptions"]]
    if project.assumptions:
        assumptions.append(project.assumptions)

    return {
        "t": t,
        "title": t["title"],
        "meta": [
            (t["project"], project.name),
            (t["client"], project.client or "—"),
            (t["date"], date.today().isoformat()),
            (t["version"], version),
        ],
        "goal": project.goal or t["none"],
        "benefit": project.benefit or t["none"],
        "param_header": [t["parameter"], t["count"], t["definition"]],
        "param_rows": param_rows,
        "fp_header": [t["parameter"], t["ifpug_type"], t["count"], t["counted"],
                      t["complexity"], t["weight"], t["fp"]],
        "fp_rows": fp_rows,
        "fp_summary": [
            (t["unadjusted"], _fmt(fp["unadjusted"])),
            (t["vaf"], f"{fp['vaf']:.2f}"),
            (t["adjusted"], _fmt(fp["adjusted"])),
        ],
        "variant_header": [t["variant"], t["productivity"], f"{t['hours']} ({t['best']})",
                           f"{t['hours']} ({t['expected']})", f"{t['hours']} ({t['worst']})",
                           f"{t['cost']} ({t['expected']})"],
        "variant_rows": variant_rows,
        "phase_header": [t["variant"], t["phase"], t["share"], t["hours"], t["cost"]],
        "phase_rows": phase_rows,
        "ops_header": [t["variant"], t["operations"], t["cost"], t["consulting"], t["cost"]],
        "ops_rows": ops_rows,
        "token_line": token_line,
        "assumptions": assumptions,
        "out_of_scope": project.out_of_scope or t["none"],
        "history_header": [t["version"], t["session"], t["date"], t["parameter"], t["fp_total"]],
        "history_rows": history_rows,
        "no_sessions": t["no_sessions"],
        "signatures": [t["sign_client"], t["sign_contractor"]],
    }


# --- DOCX --------------------------------------------------------------------

def render_docx(data: dict) -> bytes:
    from docx import Document
    from docx.shared import Pt

    doc = Document()
    t = data["t"]

    doc.add_heading(data["title"], level=0)
    doc.add_paragraph(t["subtitle"])

    def table(header, rows):
        tab = doc.add_table(rows=1, cols=len(header))
        tab.style = "Light Grid Accent 1"
        for i, h in enumerate(header):
            cell = tab.rows[0].cells[i]
            cell.text = h
            for run in cell.paragraphs[0].runs:
                run.bold = True
        for row in rows:
            cells = tab.add_row().cells
            for i, val in enumerate(row):
                cells[i].text = str(val)
        return tab

    doc.add_heading(t["s1"], level=1)
    table(["", ""], data["meta"])

    doc.add_heading(t["s2"], level=1)
    doc.add_paragraph(f"{t['goal']}: {data['goal']}")
    doc.add_paragraph(f"{t['benefit']}: {data['benefit']}")

    doc.add_heading(t["s3"], level=1)
    table(data["param_header"], data["param_rows"])

    doc.add_heading(t["s4"], level=1)
    table(data["fp_header"], data["fp_rows"])
    for label, value in data["fp_summary"]:
        p = doc.add_paragraph()
        p.add_run(f"{label}: ").bold = True
        p.add_run(value)

    doc.add_heading(t["s5"], level=1)
    table(data["variant_header"], data["variant_rows"])
    doc.add_paragraph()
    table(data["phase_header"], data["phase_rows"])

    doc.add_heading(t["s6"], level=1)
    table(data["ops_header"], data["ops_rows"])
    doc.add_paragraph(data["token_line"])

    doc.add_heading(t["s7"], level=1)
    for a in data["assumptions"]:
        doc.add_paragraph(a, style="List Bullet")

    doc.add_heading(t["s8"], level=1)
    doc.add_paragraph(data["out_of_scope"])

    doc.add_heading(t["s9"], level=1)
    if data["history_rows"]:
        table(data["history_header"], data["history_rows"])
    else:
        doc.add_paragraph(data["no_sessions"])

    doc.add_heading(t["s10"], level=1)
    for sig in data["signatures"]:
        doc.add_paragraph("\n\n_________________________________")
        p = doc.add_paragraph(sig)
        p.runs[0].font.size = Pt(9)

    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()


